// Take first number input from user and convert it to Number
let a = Number(prompt("Enter the first number: "));

// Take second number input from user and convert it to Number
let b = Number(prompt("Enter the second number: "));

// Logical AND (&&)
// Returns true only if BOTH conditions are true
console.log(`a > 0 AND b > 0 : ${a > 0 && b > 0}`);

// Logical OR (||)
// Returns true if AT LEAST ONE condition is true
console.log(`a > 0 OR b > 0  : ${a > 0 || b > 0}`);

// Logical NOT (!)
// Reverses the boolean result of the condition
console.log(`NOT (a > 0)     : ${!(a > 0)}`);

// Logical NOT (!)
// Reverses the boolean result of the condition
console.log(`NOT (b > 0)     : ${!(b > 0)}`);
