let a = Number(prompt("Enter the first number: "));
let b = Number(prompt("Enter the second number: "));

console.log(`a > 0 AND b > 0 : ${a > 0 && b > 0}`);
console.log(`a > 0 OR b > 0  : ${a > 0 || b > 0}`);
console.log(`NOT (a > 0)     : ${!(a > 0)}`);
console.log(`NOT (b > 0)     : ${!(b > 0)}`);
