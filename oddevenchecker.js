// Program to check if a number is odd or even or zero
// Initialize a number
let n = 7;
// Check if the number is odd, even or zero and print the results
if (n == 0) {
    console.log("The number is zero.");
} else if (n % 2 == 0) {
    console.log("The number is even.");
} else {
    console.log("The number is odd.");
}