// Program to demonstrate variable declaration and initialization
// Different ways to declare variables in JavaScript
let Name = "Satya"; // let keyword to declare a variable which can be reassigned
const PI = 18; // const keyword to declare a constant variable which cannot be reassigned
var age = 18; // var keyword to declare a variable (function-swcoped, can be reassigned)
// Print the variable values to the console
console.log(Name);
console.log(PI);
console.log(age);