// Program to find the sum of the elements in array
// Initialize an array of numbers
let arr = [25, 35, 45, 55, 65, 75, 85];
// Initialize sum variable to 0
let sum = 0;
// Loop through the array to calculate the sum of elements
for (let i = 0; i < arr.length; i++) {
    // Store the sum of elements in sum variable
    sum = sum + arr[i];
}
// Print the sum of the array elements
console.log("The sum of the array elements is: " + sum);